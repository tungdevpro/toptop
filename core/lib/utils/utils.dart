import 'package:url_launcher/url_launcher.dart';

abstract class CoreUtils {
  const CoreUtils._();

  static Future<void> launchInBrowser(String url) async {
    if (!await launchUrl(
      Uri.parse(url),
      mode: LaunchMode.externalApplication,
    )) {
      throw Exception('Could not launch $url');
    }
  }
}
